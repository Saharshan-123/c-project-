#include <stdio.h>

void factorial() {
    int n, i;
    unsigned long long factorial = 1; // Use unsigned long long for potentially large factorials

    printf("Enter a non-negative integer: ");
    scanf("%d", &n);

    if (n < 0) {
        printf("Factorial is not defined for negative numbers.\n"); // Factorials are only for non-negative integers
    } else {
        for (i = 1; i <= n; i++) {
            factorial *= i; // Multiply 'factorial' by the current loop counter
        }
        printf("Factorial of %d is %llu\n", n, factorial); // %llu for unsigned long long
    }

}
